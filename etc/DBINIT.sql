-- INIT
CREATE TABLE USERS (
    -- เปลี่ยนจาก NUMBER มาเป็น RAW(16) และใช้ SYS_GUID() เจนค่าเริ่มต้นให้
    USER_ID RAW(16) DEFAULT SYS_GUID() NOT NULL,
    EMAIL VARCHAR2(150) NOT NULL,
    PASSWORD_HASH VARCHAR2(500) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_USERS PRIMARY KEY (USER_ID),
    CONSTRAINT UQ_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CHK_USERS_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);

-- 2. สร้างตาราง CATEGORIES
CREATE TABLE CATEGORIES (
    CATEGORY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    USER_ID RAW(16) NULL,
    IS_DELETED NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT PK_CATEGORIES PRIMARY KEY (CATEGORY_ID),
    CONSTRAINT FK_CATEGORIES_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT CHK_CATEGORIES_DELETED CHECK (IS_DELETED IN (0, 1))
);

-- 3. สร้างตาราง BUCKET_LIST_ITEMS
CREATE TABLE BUCKET_LIST_ITEMS (
    ITEM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOT NULL,
    USER_ID RAW(16) NOT NULL,
    CATEGORY_ID NUMBER NULL,
    TITLE VARCHAR2(150) NOT NULL,
    DESCRIPTION VARCHAR2(1000),
    PRIORITY VARCHAR2(10) NOT NULL,
    IS_COMPLETED NUMBER(1) DEFAULT 0 NOT NULL,
    COMPLETED_AT TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    IS_DELETED NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT PK_BUCKET_ITEMS PRIMARY KEY (ITEM_ID),
    CONSTRAINT FK_BUCKET_ITEMS_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    -- ใช้ ON DELETE SET NULL เมื่อลบ Category ให้ Item นั้นกลายเป็นไม่มีหมวดหมู่แทนการลบ Item ทิ้ง
    CONSTRAINT FK_BUCKET_ITEMS_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES(CATEGORY_ID) ON DELETE SET NULL,
    CONSTRAINT CHK_ITEMS_PRIORITY CHECK (PRIORITY IN ('LOW', 'MEDIUM', 'HIGH')),
    CONSTRAINT CHK_ITEMS_COMPLETED CHECK (IS_COMPLETED IN (0, 1)),
    CONSTRAINT CHK_ITEMS_DELETED CHECK (IS_DELETED IN (0, 1))
);

-- Index สำหรับความเร็วตอน Query รายการ Bucket List แยกตามรายผู้ใช้ และคัดกรองเฉพาะตัวที่ยังไม่ถูกลบ (ใช้บ่อยที่สุด)
CREATE INDEX IX_BUCKET_ITEMS_USER_DEL ON BUCKET_LIST_ITEMS(USER_ID, IS_DELETED);

-- Index สำหรับฟีเจอร์ "Pick Random" ที่ต้องสุ่มเลือกเฉพาะ Item ที่ยังไม่สำเร็จและยังไม่ถูกลบ
CREATE INDEX IX_BUCKET_ITEMS_RANDOM ON BUCKET_LIST_ITEMS(USER_ID, IS_COMPLETED, IS_DELETED);